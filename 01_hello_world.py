# Hello World - Getting Started with Python

# This is a comment in Python
# Comments start with # and are ignored by Python

# The classic first program
print("Hello, World!")

# Print multiple things
print("Welcome to Python!")
print("Let's learn together!")

# You can also print multiple items at once
print("My name is:", "Python Workshop")

# Print with different separators
print("A", "B", "C", sep="-")  # Prints: A-B-C

# Print without a newline at the end
print("This is on the same line", end=" ")
print("as this!")

# Getting input from the user
name = input("What's your name? ")
print("Nice to meet you,", name)

